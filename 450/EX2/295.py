"""   Kreirajte dvije varijable i pridijelite im proizvoljne vrijednosti – True ili
False. Ispišite rezultate logičkih operatora (and, or, not) za dvije
prethodno kreirane varijable.  """

a = True 
b = False

print(a and b)
print(a or b)
print(not a)
print(a is not b) # is not je usporedba po tipu(string, float, integer...) i vrijednosti